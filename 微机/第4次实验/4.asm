DATA SEGMENT
	BUF DW 3,1,2,4,5,7,-6,8,-8,10
	N=($-BUF)/2
DATA ENDS

CODE SEGMENT 
ASSUME CS:CODE,DS:DATA
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV CX,N
		DEC CX 
	LOOP1:
		MOV DX,CX
		MOV BX,0
	LOOP2:
		MOV AX,BUF[BX]
		CMP AX,BUF[BX+2]
		JGE L;
		XCHG AX,BUF[BX+2]
		MOV BUF[BX],AX
	L:
		ADD BX,2
		DEC CX
	JNE LOOP2
	MOV CX,DX
	LOOP LOOP1
	MOV AH,4CH
	INT 21H
	CODE ENDS
	END START